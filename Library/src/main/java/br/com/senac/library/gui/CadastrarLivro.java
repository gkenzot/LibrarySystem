    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        Livro novoLivro = new Livro();
        if (livroEdicao != null) {
            novoLivro = livroEdicao;
        } else {
            novoLivro.setStatus("disponivel");
        }
        try {
            novoLivro.setTitulo(txtTitulo.getText());
            novoLivro.setAutor(txtAutor.getText());
            novoLivro.setISBN(fmtISBN.getText());
            novoLivro.setCategoria(txtCategoria.getText());
            novoLivro.setCopia(Integer.parseInt(fmtCopia.getText()));

            LivroDAO livroDao = new LivroDAO();
            if (livroEdicao == null) {
                livroDao.cadastrar(novoLivro);
            } else {
                livroDao.atualizar(novoLivro);
            }
            this.dispose();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

