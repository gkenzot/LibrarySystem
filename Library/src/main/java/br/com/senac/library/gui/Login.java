    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        Usuario usuario = new Usuario();
        usuario.setEmail(txtEmail.getText());
        char[] senhaChars = pwSenha.getPassword();
        String senha = new String(senhaChars); // Converta para String
        usuario.setSenha(Criptografia.getMD5(senha));
        // System.out.println("A senha em MD5 é: " + usuario.getSenha());
        usuario = UsuarioBD.validarUsuarioSeguro(usuario);

        if (usuario != null) {
            String[] tiposPermitidos = {"Bibliotecario", "Usuario"};
            String permissao = usuario.getPermissao().trim(); // Removendo espaços extras

            boolean tipoValido = false;
            for (String tipo : tiposPermitidos) {
                if (permissao.equalsIgnoreCase(tipo)) {
                    tipoValido = true;
                    break;
                }
            }
            if (tipoValido) {
                JOptionPane.showMessageDialog(null, "Olá " + usuario.getNome() + ", sua permissão é de " + usuario.getPermissao() + ". Seja bem-vindo!");
                // Passa as informações do usuário logado para a tela de listagem
                MainMenu listagem = new MainMenu();
                listagem.configurarPermissoes(permissao);
                listagem.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Ops, " + usuario.getNome() + "\nParece que você não tem permissão para acessar o sistema");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Erro de autenticação! Verifique se os dados estão corretos.");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

