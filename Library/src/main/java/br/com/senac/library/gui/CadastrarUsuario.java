    public void preencheEdicao(Usuario u) {
        txtNome.setText(u.getNome());
        fmtCPF.setText(u.getCPF());
        txtEmail.setText(u.getEmail());
        txtTelefone.setText(u.getTelefone());
        txtEndereco.setText(u.getEndereco());
        if (u.getPermissao().equals("Bibliotecario")) {
            rbtnBibliotecario.setSelected(true);
            rbtnUsuario.setSelected(false);
        } else {
            rbtnBibliotecario.setSelected(false);
            rbtnUsuario.setSelected(true);
        }
        usuarioEdicao = u;
    }

