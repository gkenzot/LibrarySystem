    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        Usuario novoUsuario = new Usuario();
        if (usuarioEdicao != null) {
            novoUsuario = usuarioEdicao;
        }

        try {
            novoUsuario.setNome(txtNome.getText());
            novoUsuario.setCPF(fmtCPF.getText());
            novoUsuario.setEmail(txtEmail.getText());
            novoUsuario.setTelefone(txtTelefone.getText());
            novoUsuario.setEndereco(txtEndereco.getText());

            // Radio button
            if (rbtnBibliotecario.isSelected()) {
                novoUsuario.setPermissao("Bibliotecario");
            } else {
                novoUsuario.setPermissao("Usuario");
            }

            // Senha
            String senha1 = new String(pwSenha1.getPassword());
            String senha2 = new String(pwSenha2.getPassword());

            if (!senha1.isEmpty() && !senha2.isEmpty()) {
                if (senha1.equals(senha2)) {
                    String senhaCriptografada = Criptografia.getMD5(senha1);
                    novoUsuario.setSenha(senhaCriptografada);
                } else {
                    JOptionPane.showMessageDialog(this, "As senhas não coincidem.");
                    return;
                }
            } else {
                // Mantém a senha atual
                novoUsuario.setSenha(usuarioEdicao.getSenha());
            }

            UsuarioDAO usuarioDao = new UsuarioDAO();
            if (usuarioEdicao == null) {
                usuarioDao.cadastrar(novoUsuario);
            } else {
                usuarioDao.atualizar(novoUsuario);
            }
            this.dispose();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ocorreu uma falha:\n" + e.getMessage());
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

