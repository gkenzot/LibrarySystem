    /**
     * Configura as permissões com base no tipo de usuário.
     *
     * @param tipoUsuario O tipo de usuário logado.
     */
    public void configurarPermissoes(String tipoUsuario) {
        if (tipoUsuario.equalsIgnoreCase("Bibliotecario")) {
            btnConsultar.setEnabled(true);
            btnCadastrarLivro.setEnabled(true);
            btnListagemLivros.setEnabled(true);
            btnCadastrarUsuario.setEnabled(true);
            btnListagemUsuarios.setEnabled(true);
        } else if (tipoUsuario.equalsIgnoreCase("Usuario")) {
            btnConsultar.setEnabled(true);
            btnCadastrarLivro.setEnabled(false);
            btnListagemLivros.setEnabled(false);
            btnCadastrarUsuario.setEnabled(false);
            btnListagemUsuarios.setEnabled(false);
        } else {
            btnConsultar.setEnabled(false);
            btnCadastrarLivro.setEnabled(false);
            btnListagemLivros.setEnabled(false);
            btnCadastrarUsuario.setEnabled(false);
            btnListagemUsuarios.setEnabled(false);
        }
    }

